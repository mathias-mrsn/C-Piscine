int	ft_sqrt(int nb)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (n != nb && i <= 46340)
	{
		n = i * i;
		if (n > nb)
			return (0);
		else if (n == nb)
			return (i);
		i++;
	}
	return (0);
}
